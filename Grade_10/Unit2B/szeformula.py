#formula for area of a triangle
def area_triangle(base,height):
    area = base * height * 0.5
    return area

#formula for perimeter of the triangle
def perimeter_triangle(side_1,side_2,side_3):
    perimeter = side_1 + side_2 + side_3
    return perimeter

#formula for area of a trapezoid
def area_trapezoid(base,height,oppbase):
    area = (base + oppbase)* height * 0.5
    return area

#formula for perimeter of the trapezoid
def perimeter_trapezoid(side_1,side_2,side_3,side_4):
    perimeter = side_1 + side_2 + side_3 + side_4
    return perimeter
    
    
